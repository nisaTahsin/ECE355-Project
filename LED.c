void refresh_OLED( void )
{
    Freq++;
    Res++;
    // Buffer size = at most 16 characters per PAGE + terminating '\0'
    unsigned char Buffer[17];

    snprintf( Buffer, sizeof( Buffer ), "R: %5u Ohms", Res );
    /* Buffer now contains your character ASCII codes for LED Display
       - select PAGE (LED Display line) and set starting SEG (column)
       - for each c = ASCII code = Buffer[0], Buffer[1], ...,
           send 8 bytes in Characters[c][0-7] to LED Display
    */
    /* starting at page 1, column 3 */
    oled_Write_Cmd(0xB1);
    // lower 3, upper 0
    oled_Write_Cmd(0x03);
    oled_Write_Cmd(0x10);

    /* for 16 characters in buffer */
    for(int i = 0; i < 16; i++){
        /* for 8 bytes sent to LED Display */
        for(int j = 0; j < 8; j++){
            /* Find graphical data for character in buffer, assigning it to variable character */
            unsigned char character = Characters[Buffer[i]][j];
            /* send character to OLED display */
            oled_Write_Data(character);
        }
    }


    snprintf( Buffer, sizeof( Buffer ), "F: %5u Hz", Freq );
    /* Buffer now contains your character ASCII codes for LED Display
       - select PAGE (LED Display line) and set starting SEG (column)
       - for each c = ASCII code = Buffer[0], Buffer[1], ...,
           send 8 bytes in Characters[c][0-7] to LED Display
    */

    /* Write to page 3, starting column 3 */
    oled_Write_Cmd(0xB3);
    oled_Write_Cmd(0x03);
    oled_Write_Cmd(0x10);

    /* same loop as before to write frequency to page 3 */
    for(int i = 0; i < 16; i++){
        for(int j = 0; j < 8; j++){
            unsigned char character = Characters[Buffer[i]][j];
            oled_Write_Data(character); // send char pixel data to OLED
        }
    }

    /* fill buffer with source of wave info */
    if(measuring555){
        snprintf( (char*)Buffer, sizeof( Buffer ), "  Source: 555   " );
    }else{
        snprintf( (char*)Buffer, sizeof( Buffer ), "  Source: FG    " );
    }

    oled_Write_Cmd(0xB5);
    oled_Write_Cmd(0x03);
    oled_Write_Cmd(0x10);

    /* same loop as before to write source to page 5 */
    for(int i = 0; i < 16; i++){
        for(int j = 0; j < 8; j++){
            unsigned char character = Characters[Buffer[i]][j];
            oled_Write_Data(character);
        }
    }

	/* Wait for ~100 ms (for example) to get ~10 frames/sec refresh rate
       - You should use TIM3 to implement this delay (e.g., via polling)
    */

    for(int delay = 0; delay <10000; delay++){}

}


void oled_Write_Cmd( unsigned char cmd )
{
    GPIOB->ODR |= GPIO_ODR_8; // make PB8 = CS# = 1
    GPIOB->ODR &= ~(GPIO_ODR_9); // make PB9 = D/C# = 0
    GPIOB->ODR &= ~(GPIO_ODR_8); // make PB8 = CS# = 0
    oled_Write( cmd );
    GPIOB->ODR |= GPIO_ODR_8; // make PB8 = CS# = 1
}

void oled_Write_Data( unsigned char data )
{
    GPIOB->ODR |= GPIO_ODR_8; // make PB8 = CS# = 1
    GPIOB->ODR |= GPIO_ODR_9; // make PB9 = D/C# = 1
    GPIOB->ODR &= ~(GPIO_ODR_8); // make PB8 = CS# = 0
    oled_Write( data );
    GPIOB->ODR |= GPIO_ODR_8; // make PB8 = CS# = 1
}


void oled_Write( unsigned char Value )
{

    /* Wait until SPI2 is ready for writing (TXE = 1 in SPI2_SR) */
    while((SPI2->SR & SPI_SR_TXE) == 0 );

    /* Send one 8-bit character:
       - This function also sets BIDIOE = 1 in SPI2_CR1
    */
    HAL_SPI_Transmit( &SPI_Handle, &Value, 1, HAL_MAX_DELAY );


    /* Wait until transmission is complete (TXE = 1 in SPI2_SR) */
    /* Wait until busy flag to go low to indicate transmission complete */
    while((SPI2->SR & SPI_SR_BSY) != 0 );

}


void oled_config( void )
{

// Don't forget to enable GPIOB clock in RCC
// Don't forget to configure PB13/PB15 as AF0
// Don't forget to enable SPI2 clock in RCC

    SPI_Handle.Instance = SPI2;

    SPI_Handle.Init.Direction = SPI_DIRECTION_1LINE;
    SPI_Handle.Init.Mode = SPI_MODE_MASTER;
    SPI_Handle.Init.DataSize = SPI_DATASIZE_8BIT;
    SPI_Handle.Init.CLKPolarity = SPI_POLARITY_LOW;
    SPI_Handle.Init.CLKPhase = SPI_PHASE_1EDGE;
    SPI_Handle.Init.NSS = SPI_NSS_SOFT;
    SPI_Handle.Init.BaudRatePrescaler = SPI_BAUDRATEPRESCALER_256;
    SPI_Handle.Init.FirstBit = SPI_FIRSTBIT_MSB;
    SPI_Handle.Init.CRCPolynomial = 7;

//
// Initialize the SPI interface
//
    HAL_SPI_Init( &SPI_Handle );

//
// Enable the SPI
//
    __HAL_SPI_ENABLE( &SPI_Handle );


    /* Reset LED Display (RES# = PB11):
       - make pin PB11 = 0, wait for a few ms
       - make pin PB11 = 1, wait for a few ms
    */

    /* setting PB11 = 0 */
    GPIOB->ODR &= ~(GPIO_ODR_11);

    /* adding small delay with a loop */
    for(unsigned int i=0;i<100000;i++){}

    /* setting PB11 = 1 */
    GPIOB->ODR |= GPIO_ODR_11;

    /* adding small delay with a loop */
    for(unsigned int i=0;i<100000;i++){}

//
// Send initialization commands to LED Display
//
    for ( unsigned int i = 0; i < sizeof( oled_init_cmds ); i++ )
    {
        oled_Write_Cmd( oled_init_cmds[i] );
    }


    /* Fill LED Display data memory (GDDRAM) with zeros:
       - for each PAGE = 0, 1, ..., 7
           set starting SEG = 0
           call oled_Write_Data( 0x00 ) 128 times
    */

    for(int page = 0xB0; page <= 0xB7; page++)
    {
        /* write zeros starting from column 0 for all pages */
        oled_Write_Cmd(page);
        oled_Write_Cmd(0x00);
        oled_Write_Cmd(0x10);

        /* each page has 128 segments 0-127 */
        for (int segment = 0; segment < 128; segment++){
            oled_Write_Data( 0x00 );
        }
    }

}

/* Interrupt handler for user button (PA0) */
void EXTI0_1_IRQHandler(void)
{
    if (EXTI->PR & EXTI_PR_PR0) {

        /* Wait for button to be released (PA0 = 0) */
        while((GPIOA->IDR & GPIO_IDR_0) != 0) {
            __NOP();
        }

        /* Switch measuring555 flag */
        measuring555 = !measuring555;

        /* Print message based on which source is being measured */
        if (measuring555) {
            Freq = 3000;
            Res = 5000;
            trace_printf("Measuring 555 Timer (PB3)\n");
        } else {
            Freq = 2000;
            Res = 1000;
            trace_printf("Measuring Function Generator (PB2)\n");
        }

        /* Clear EXTI0 interrupt pending flag */
        EXTI->PR = EXTI_PR_PR0;
    }

}



